# Faça um Programa que peça as 4 notas bimestrais e mostre a média.
nota1 = float(input('Entre com a primeira nota bimestral: '))
nota2 = float(input('Entre com a segunda nota bimestral: '))
nota3 = float(input('Entre com a terceira nota bimestral: '))
nota4 = float(input('Entre com a quarta nota bimestral: '))
media = (nota1 + nota2 + nota3 + nota4)/4
print(f'A sua média foi de : {media}')
